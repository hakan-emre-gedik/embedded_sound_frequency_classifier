SCREEN_SEND_CHAR_ PROC
			LDR		R1, =GPIO_PORTD_DATA 
			LDR		R2, [R1]
			ORR		R2, #0xC0 
			STR		R2, [R1]
			
			LDR		R1, =SSI1_DATA
			LDR		R5, =SSI1_SR
check 		LDR		R4, [R5]
			ANDS	R4, 0x10 ; IS SSI BUSY? 
			BNE		check 
			STR		R0, [R1] 
			
			BX LR 
			ENDP 